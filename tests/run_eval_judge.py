async def run_pipeline_for_query(query: str, doc_filter: Optional[List[UUID]] = None) -> QueryPipelineContext:
    """
    Executes the complete 7-stage retrieval-generation pipeline for a query.
    
    Args:
        query (str): The query string.
        doc_filter (Optional[List[UUID]]): Optional list of active document IDs to filter by.
        
    Returns:
        QueryPipelineContext: The populated context after generator stage completes.
    """
    ctx = QueryPipelineContext(
        request_id=str(uuid4()),
        original_query=query,
        doc_filter=doc_filter
    )
    
    ctx = await run_query_expander(ctx)
    ctx = await run_hybrid_search(ctx)
    ctx = await run_hop_expander(ctx)
    ctx = await run_temporal_filter(ctx)
    ctx = await run_reranker(ctx)
    ctx = await run_compressor(ctx)
    
    # Run generator to populate ctx.answer_text
    async for _ in run_generator(ctx):
        pass
        
    return ctx

async def evaluate_with_judge(
    ollama_client: AsyncClient,
    model: str,
    query: str,
    context: str,
    generated_answer: str,
    golden_answer: str,
    semaphore: asyncio.Semaphore
) -> Dict[str, float]:
    """
    Uses the Judge LLM to evaluate the generated response against context and golden reference.
    
    Args:
        ollama_client (AsyncClient): Ollama async client.
        model (str): Judge model name.
        query (str): User query.
        context (str): Retrieved/compressed context.
        generated_answer (str): Answer generated by the system.
        golden_answer (str): Golden reference answer.
        semaphore (asyncio.Semaphore): Concurrency controller.
        
    Returns:
        Dict[str, float]: Dict containing faithfulness, context_recall, answer_relevance, and citation_precision scores.
    """
    system_prompt = (
        "You are an independent, highly critical regulatory evaluation judge. Your task is to evaluate a generated answer.\n"
        "Ignore any formatting mismatches, bulleting styles, layout structure (such as tables vs. lists), or document length differences between the generated answer and the golden reference answer. "
        "Evaluate strictly based on semantic facts, compliance logic, factual accuracy, and faithfulness.\n"
        "You MUST output ONLY a JSON object with exactly four keys:\n"
        "- 'faithfulness': float (0.0 to 1.0). Is every claim in the generated answer fully and directly supported by the context? Deduct heavily for any claim not found in the context.\n"
        "- 'context_recall': float (0.0 to 1.0). Are all the key facts in the golden reference answer present in the retrieved context?\n"
        "- 'answer_relevance': float (0.0 to 1.0). Does the generated answer directly and completely address the question, or is it vague/evasive?\n"
        "- 'citation_precision': float (0.0 to 1.0). Are the sections cited in the generated answer actually present in the context? Score 1.0 if all citations match context, 0.0 if there are missing/hallucinated citations.\n\n"
        "Example output format:\n"
        "{\n"
        "  \"faithfulness\": 1.0,\n"
        "  \"context_recall\": 0.9,\n"
        "  \"answer_relevance\": 0.8,\n"
        "  \"citation_precision\": 1.0\n"
        "}"
    )
    
    user_prompt = (
        f"QUESTION: {query}\n\n"
        f"RETRIEVED CONTEXT:\n{context}\n\n"
        f"GENERATED ANSWER:\n{generated_answer}\n\n"
        f"GOLDEN REFERENCE ANSWER:\n{golden_answer}\n\n"
        "Evaluate the generated answer. Output strictly JSON conforming to the schema."
    )
    
    async with semaphore:
        try:
            response = await ollama_client.chat(
                model=model,
                messages=[
                    {"role": "system", "content": system_prompt},
                    {"role": "user", "content": user_prompt}
                ],
                format="json",
                options={"temperature": 0.0}
            )
            content_str = response.message.content
            parsed = json.loads(content_str)
            
            return {
                "faithfulness": float(parsed.get("faithfulness", 0.0)),
                "context_recall": float(parsed.get("context_recall", 0.0)),
                "answer_relevance": float(parsed.get("answer_relevance", 0.0)),
                "citation_precision": float(parsed.get("citation_precision", 0.0))
            }
        except Exception as e:
            logger.debug("judge_evaluation_failed", error=str(e))
            return {
                "faithfulness": 0.0,
                "context_recall": 0.0,
                "answer_relevance": 0.0,
                "citation_precision": 0.0
            }

async def process_single_eval(
    ollama_client: AsyncClient,
    judge_model: str,
    item: Dict[str, Any],
    pipeline_semaphore: asyncio.Semaphore,
    judge_semaphore: asyncio.Semaphore
) -> Dict[str, Any]:
    """
    Runs retrieval-generation pipeline for a single golden item, computes metrics,
    and runs the Judge LLM evaluation.
    
    Args:
        ollama_client (AsyncClient): Ollama async client.
        judge_model (str): Judge model name.
        item (Dict[str, Any]): A single golden dataset item.
        pipeline_semaphore (asyncio.Semaphore): Pipeline concurrency controller.
        judge_semaphore (asyncio.Semaphore): Judge concurrency controller.
        
    Returns:
        Dict[str, Any]: Detailed evaluation record.
    """
    query = item["query"]
    golden_answer = item["golden_answer"]
    target_node_id_str = item.get("target_node_id")
    category = item.get("category", "General")
    
    start_time = time.monotonic()
    
    # 1. Run pipeline
    async with pipeline_semaphore:
        ctx = await run_pipeline_for_query(query)
    latency = time.monotonic() - start_time
    
    # 2. Compute Retrieval Metrics (Recall & MRR)
    recall_at_5 = 0.0
    recall_at_10 = 0.0
    mrr = 0.0
    
    if target_node_id_str:
        try:
            target_node_uuid = UUID(target_node_id_str)
            # Find in candidate nodes
            candidate_ids = [node.node_id for node in ctx.candidate_nodes]
            
            if target_node_uuid in candidate_ids[:5]:
                recall_at_5 = 1.0
            if target_node_uuid in candidate_ids[:10]:
                recall_at_10 = 1.0
                
            if target_node_uuid in candidate_ids:
                rank = candidate_ids.index(target_node_uuid) + 1
                mrr = 1.0 / rank
        except ValueError:
            pass
            
    # 3. Call Judge LLM for generation quality
    judge_scores = await evaluate_with_judge(
        ollama_client=ollama_client,
        model=judge_model,
        query=query,
        context=ctx.compressed_context,
        generated_answer=ctx.answer_text,
        golden_answer=golden_answer,
        semaphore=judge_semaphore
    )
    
    return {
        "query": query,
        "category": category,
        "target_node_id": target_node_id_str,
        "latency_sec": latency,
        "retrieval": {
            "recall_at_5": recall_at_5,
            "recall_at_10": recall_at_10,
            "mrr": mrr,
            "candidates_returned": len(ctx.candidate_nodes)
        },
        "generation": judge_scores,
        "generated_answer": ctx.answer_text,
        "golden_answer": golden_answer
    }

async def main() -> None:
    parser = argparse.ArgumentParser(description="Evaluate the RAG pipeline using LLM-as-a-Judge.")
    parser.add_argument("--dataset", type=str, default="tests/golden_dataset.json", help="Golden dataset JSON path.")
    parser.add_argument("--judge-model", type=str, default="mistral-nemo:12b", help="Ollama model to use as Judge.")
    parser.add_argument("--concurrency", type=int, default=4, help="Concurrency limit for judge calls.")
    parser.add_argument("--pipeline-concurrency", type=int, default=1, help="Concurrency limit for pipeline runs (highly recommended to keep at 1 or 2 for local Ollama).")
    parser.add_argument("--limit", type=int, default=0, help="Limit number of evaluation items (0 for all).")
    parser.add_argument("--output", type=str, default="tests/eval_results.json", help="Detailed results output path.")
    args = parser.parse_args()
    
    logger.info("evaluation_runner_started", dataset=args.dataset, judge=args.judge_model, limit=args.limit)
    
    if not os.path.exists(args.dataset):
        logger.error("dataset_file_not_found", path=args.dataset)
        sys.exit(1)
        
    with open(args.dataset, "r", encoding="utf-8") as f:
        dataset = json.load(f)
        
    if args.limit > 0:
        dataset = dataset[:args.limit]
        
    logger.info("loaded_dataset_items", count=len(dataset))
    
    # Initialize DB pool
    await init_db_pool()
    
    ollama_client = AsyncClient(host=OLLAMA_HOST)
    pipeline_semaphore = asyncio.Semaphore(args.pipeline_concurrency)
    judge_semaphore = asyncio.Semaphore(args.concurrency)
    
    tasks = [
        process_single_eval(ollama_client, args.judge_model, item, pipeline_semaphore, judge_semaphore)
        for item in dataset
    ]
    
    records = []
    completed = 0
    
    logger.info("running_eval_pipeline_tasks", total_tasks=len(tasks))
    
    for future in asyncio.as_completed(tasks):
        record = await future
        records.append(record)
        completed += 1
        
        logger.info("eval_item_complete", 
                    completed=completed, 
                    total=len(tasks), 
                    query=record["query"][:50], 
                    recall_10=record["retrieval"]["recall_at_10"],
                    faithfulness=record["generation"]["faithfulness"])
                    
    # Clean up DB pool
    await close_db_pool()
    
    # 4. Aggregate metrics
    count = len(records)
    if count == 0:
        logger.error("no_evaluation_results")
        sys.exit(1)
        
    avg_recall_5 = sum(r["retrieval"]["recall_at_5"] for r in records) / count
    avg_recall_10 = sum(r["retrieval"]["recall_at_10"] for r in records) / count
    avg_mrr = sum(r["retrieval"]["mrr"] for r in records) / count
    avg_latency = sum(r["latency_sec"] for r in records) / count
    
    avg_faithfulness = sum(r["generation"]["faithfulness"] for r in records) / count
    avg_context_recall = sum(r["generation"]["context_recall"] for r in records) / count
    avg_relevance = sum(r["generation"]["answer_relevance"] for r in records) / count
    avg_citation_precision = sum(r["generation"]["citation_precision"] for r in records) / count
    
    # Category level aggregation
    cat_stats: Dict[str, Dict[str, Any]] = {}
    for r in records:
        cat = r["category"]
        if cat not in cat_stats:
            cat_stats[cat] = {"count": 0, "recall_10": 0.0, "faithfulness": 0.0}
        cat_stats[cat]["count"] += 1
        cat_stats[cat]["recall_10"] += r["retrieval"]["recall_at_10"]
        cat_stats[cat]["faithfulness"] += r["generation"]["faithfulness"]
        
    for cat, stats in cat_stats.items():
        stats["avg_recall_10"] = stats["recall_10"] / stats["count"]
        stats["avg_faithfulness"] = stats["faithfulness"] / stats["count"]

    summary = {
        "total_evaluated": count,
        "average_latency_sec": round(avg_latency, 3),
        "retrieval": {
            "avg_recall_at_5": round(avg_recall_5, 4),
            "avg_recall_at_10": round(avg_recall_10, 4),
            "avg_mrr": round(avg_mrr, 4)
        },
        "generation": {
            "avg_faithfulness": round(avg_faithfulness, 4),
            "avg_context_recall": round(avg_context_recall, 4),
            "avg_relevance": round(avg_relevance, 4),
            "avg_citation_precision": round(avg_citation_precision, 4)
        },
        "by_category": cat_stats
    }
    
    # Print results summary to console
    print("\n" + "="*50)
    print("             EVALUATION REPORT SUMMARY")
    print("="*50)
    print(f"Total Evaluated Questions: {summary['total_evaluated']}")
    print(f"Average Latency:           {summary['average_latency_sec']:.3f} seconds")
    print("-"*50)
    print("RETRIEVED QUALITY (STAGE B):")
    print(f"  Recall@5:                {summary['retrieval']['avg_recall_at_5']:.4%}")
    print(f"  Recall@10:               {summary['retrieval']['avg_recall_at_10']:.4%}")
    print(f"  Mean Reciprocal Rank:    {summary['retrieval']['avg_mrr']:.4f}")
    print("-"*50)
    print("GENERATED QUALITY (LLM-AS-A-JUDGE):")
    print(f"  Faithfulness (No Halluc): {summary['generation']['avg_faithfulness']:.4%}")
    print(f"  Context Recall:          {summary['generation']['avg_context_recall']:.4%}")
    print(f"  Answer Relevance:        {summary['generation']['avg_relevance']:.4%}")
    print(f"  Citation Precision:      {summary['generation']['avg_citation_precision']:.4%}")
    print("-"*50)
    print("PER-CATEGORY BREAKDOWN:")
    for cat, stats in cat_stats.items():
        print(f"  [{cat}] (n={stats['count']})")
        print(f"    Recall@10:             {stats['avg_recall_10']:.4%}")
        print(f"    Faithfulness:          {stats['avg_faithfulness']:.4%}")
    print("="*50 + "\n")
    
    # Save detailed results to JSON
    with open(args.output, "w", encoding="utf-8") as out:
        json.dump({
            "summary": summary,
            "detailed_runs": records
        }, out, indent=2, ensure_ascii=False)
    logger.info("saved_evaluation_detailed_results", path=args.output)

if __name__ == "__main__":
    asyncio.run(main())
